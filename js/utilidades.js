class Proyecto {
    constructor(codigo, titulo, area, estado) {
        this.codigo = codigo;
        this.titulo = titulo;
        this.area = area;
        this.estado = estado;
    }
}

class Programa {
    constructor(codigo, nombre, duracion, modalidad) {
        this.codigo = codigo;
        this.nombre = nombre;
        this.duracion = duracion;
        this.modalidad = modalidad;
    }
}